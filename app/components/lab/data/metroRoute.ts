export type RouteType =
  | "walking"
  | "surface"
  | "elevated"
  | "subterranean"
  | "transition"
  | "transfer"

export type WeatherType = "clear" | "cloudy" | "rain"
export type ThermalType = "cool" | "neutral" | "warm" | "hot"
export type LabEvent = "crowded" | "peak" | "rain" | "transition" | "noise" | "none"

export type RoutePoint = {
  id: string
  name: string
  segment: string
  line: string
  lat: number
  lng: number
  type: RouteType

  pm25: number
  humidity: number
  temp: number
  db: number
  crowd: number

  transitionLabel?: string
  weather: WeatherType
  wind: number
  thermal: ThermalType
  event: LabEvent

  rain?: boolean
  peak?: boolean
  observation?: string
}

export function pmColor(pm: number) {
  if (pm <= 8) return "#22c55e"
  if (pm <= 12) return "#84cc16"
  if (pm <= 18) return "#eab308"
  if (pm <= 26) return "#f97316"
  return "#ef4444"
}

export function pmLabel(pm: number) {
  if (pm <= 8) return "Muy bajo"
  if (pm <= 12) return "Bajo"
  if (pm <= 18) return "Moderado"
  if (pm <= 26) return "Elevado relativo"
  return "Muy elevado"
}

export function typeLabel(type: RouteType) {
  if (type === "walking") return "Exterior"
  if (type === "surface") return "Superficie"
  if (type === "elevated") return "Elevado"
  if (type === "subterranean") return "Subterráneo"
  if (type === "transition") return "Transición"
  if (type === "transfer") return "Combinación"
  return type
}

export function weatherLabel(weather: WeatherType) {
  if (weather === "clear") return "Despejado"
  if (weather === "cloudy") return "Nublado"
  if (weather === "rain") return "Lluvia"
  return "Sin clima"
}

export function thermalLabel(thermal: ThermalType) {
  if (thermal === "cool") return "Frío / fresco"
  if (thermal === "neutral") return "Neutro"
  if (thermal === "warm") return "Cálido"
  if (thermal === "hot") return "Caluroso"
  return "Sin dato"
}

export function peopleIcons(crowd: number) {
  return "👤".repeat(Math.max(1, Math.min(10, Math.ceil(crowd / 10))))
}

